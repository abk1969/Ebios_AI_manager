/**
 * Scan des am√©liorations Phase 3
 */
function scanPhase3Improvements() {
  console.log('\nüîç SCAN DES AM√âLIORATIONS PHASE 3...');
  
  let totalImprovements = 0;
  let improvedFiles = 0;
  
  PHASE3_FILES.forEach(file => {
    if (fs.existsSync(file)) {
      const content = fs.readFileSync(file, 'utf8');
      
      // Compter les am√©liorations
      const improvements = [
        (content.match(/\/\/ Donn√©es r√©elles/g) || []).length,
        (content.match(/\/\/ Impl√©ment√©/g) || []).length,
        (content.match(/real[A-Z]/g) || []).length,
        (content.match(/crypto\.randomUUID\(\)/g) || []).length,
        (content.match(/new Date\(Date\.now\(\)/g) || []).length,
        (content.match(/\/\/ console\.log supprim√©/g) || []).length,
        (content.match(/\/\* Donn√©es r√©elles \*\//g) || []).length
      ];
      
      const fileImprovements = improvements.reduce((a, b) => a + b, 0);
      if (fileImprovements > 0) {
        console.log(`   üìÑ ${path.basename(file)}: ${fileImprovements} am√©liorations`);
        totalImprovements += fileImprovements;
        improvedFiles++;
      }
    }
  });
  
  console.log(`   üìä Total am√©liorations: ${totalImprovements}`);
  console.log(`   üìÅ Fichiers am√©lior√©s: ${improvedFiles}`);
  
  return { totalImprovements, improvedFiles };
}

/**
 * Restauration d'urgence Phase 3
 */
function emergencyRestorePhase3() {
  console.log('\nüö® RESTAURATION D\'URGENCE PHASE 3...');
  
  try {
    const backupFiles = [];
    
    function findPhase3Backups(dir) {
      const files = fs.readdirSync(dir);
      files.forEach(file => {
        const filePath = path.join(dir, file);
        const stat = fs.statSync(filePath);
        
        if (stat.isDirectory() && !['node_modules', '.git', 'dist'].includes(file)) {
          findPhase3Backups(filePath);
        } else if (file.includes('.backup-phase3-')) {
          backupFiles.push(filePath);
        }
      });
    }
    
    findPhase3Backups('.');
    
    backupFiles.forEach(backupFile => {
      const originalFile = backupFile.replace(/\.backup-phase3-\d+$/, '');
      if (fs.existsSync(backupFile)) {
        fs.copyFileSync(backupFile, originalFile);
        fs.unlinkSync(backupFile);
        console.log(`   üîÑ Restaur√©: ${path.basename(originalFile)}`);
      }
    });
    
    console.log(`   ‚úÖ ${backupFiles.length} fichiers restaur√©s`);
  } catch (error) {
    console.error(`   ‚ùå Erreur restauration: ${error.message}`);
  }
}

